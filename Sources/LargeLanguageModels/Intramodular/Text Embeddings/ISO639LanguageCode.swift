extension ISO639LanguageCode {
    private static let _byStrCode: [String: ISO639LanguageCode] = {
        var result: [String: ISO639LanguageCode] = [:]
        
        for `case` in ISO639LanguageCode.allCases {
            result[`case`.rawValue] = `case`
        }
        
        return result
    }()
    
    static func byStrCode(_ strCode: String) -> ISO639LanguageCode? {
        _byStrCode[strCode.uppercased()]
    }
}
